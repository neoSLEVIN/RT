#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned const char	*ps;
	unsigned char		pc;

	pc = (unsigned char)c;
	ps = (unsigned const char *)s;
	while (n--)
	{
		if (*ps == pc)
			return ((void *)ps);
		ps++;
	}
	return (NULL);
}
