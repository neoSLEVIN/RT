#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	if ((size + 1) == 0)
		return (NULL);
	str = (char *)ft_memalloc(size + 1);
	if (str == NULL)
		return (NULL);
	return (str);
}
