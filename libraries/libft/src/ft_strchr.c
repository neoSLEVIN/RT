#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	unsigned char	ic;

	ic = (unsigned char)c;
	while (*s && *s != ic)
		s++;
	if (*s == ic)
		return ((char *)s);
	return (NULL);
}
