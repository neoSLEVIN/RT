#include "ocl_math.h"

FLT3	v3_norm(FLT3 v)
{
	cl_float	len;

	len = v3_length(v);
	if (len > 0)
		return (v3_scale(v, 1.0 / len));
	return (cl_float3){0,0,0};
}
